// 교차 해석 (복수 영역 동시 이상 시)
export function getCrossInterpretation(results: { scaleId: string; levelIdx: number }[]): string[] {
  const interpretations: string[] = []
  const get = (id: string) => results.find((r) => r.scaleId === id)

  const dep = get('depression')
  const anx = get('anxiety')
  const stress = get('stress')
  const self = get('selfesteem')
  const sleep = get('insomnia')

  // 우울 + 불안 동반
  if (dep && anx && dep.levelIdx >= 2 && anx.levelIdx >= 2) {
    interpretations.push('우울과 불안이 동시에 중등도 이상으로 관찰됩니다. 이 두 증상은 흔히 동반(공존)되며, 하나의 증상이 다른 증상을 악화시킬 수 있습니다. 통합적인 치료적 접근이 필요합니다.')
  }

  // 우울 + 자존감 저하
  if (dep && self && dep.levelIdx >= 1 && self.levelIdx >= 2) {
    interpretations.push('우울 증상과 함께 자존감 저하가 관찰됩니다. 낮은 자존감은 우울의 원인이자 결과가 될 수 있으며, 부정적 자기 인식을 교정하는 인지치료가 도움이 될 수 있습니다.')
  }

  // 수면 + 우울/불안
  if (sleep && sleep.levelIdx >= 2 && ((dep && dep.levelIdx >= 1) || (anx && anx.levelIdx >= 1))) {
    interpretations.push('수면 문제와 정서적 어려움이 동반되고 있습니다. 불면은 우울·불안의 원인이자 증상일 수 있으며, 수면 개선이 정서 회복에도 중요한 역할을 합니다.')
  }

  // 스트레스 + 수면
  if (stress && sleep && stress.levelIdx >= 2 && sleep.levelIdx >= 1) {
    interpretations.push('높은 스트레스와 수면 문제가 동시에 나타나고 있습니다. 만성 스트레스는 수면의 질을 저하시키고, 수면 부족은 다시 스트레스 내성을 약화시키는 악순환을 형성할 수 있습니다.')
  }

  return interpretations
}
