// 교차 해석
export function getCrossInterpretation(results: { scaleId: string; levelIdx: number }[]): string[] {
  const notes: string[] = []
  const get = (id: string) => results.find((r) => r.scaleId === id)
  const dep = get('depression'), anx = get('anxiety'), stress = get('stress'), self = get('selfesteem'), sleep = get('insomnia')

  if (dep && anx && dep.levelIdx >= 2 && anx.levelIdx >= 2)
    notes.push('우울 및 불안 증상이 동시에 중등도 이상으로 관찰되며, 두 증상 간 상호 악화 양상이 형성되어 있는 것으로 판단됩니다. 우울로 인한 반추적 사고가 불안을 촉발하고, 불안으로 인한 에너지 소진이 우울을 심화시키는 구조로, 한쪽 증상만을 다룰 경우 치료 효과가 제한적일 수 있어 두 증상을 통합적으로 다루는 전문적 개입이 필요한 것으로 사료됩니다.')

  if (dep && self && dep.levelIdx >= 1 && self.levelIdx >= 2)
    notes.push('우울 증상과 함께 자존감의 유의미한 저하가 관찰됩니다. 부정적 자기 인식이 우울 삽화를 유지하는 핵심 요인으로 작용하고 있을 가능성이 있으며, 인지행동치료를 통한 부정적 사고 패턴의 수정이 우울 회복과 자존감 향상 모두에 기여할 수 있을 것으로 판단됩니다.')

  if (sleep && sleep.levelIdx >= 2 && ((dep && dep.levelIdx >= 1) || (anx && anx.levelIdx >= 1)))
    notes.push('수면 문제와 정서적 어려움이 동반되어 있으며, 양방향 악순환 구조가 형성되어 있는 것으로 보입니다. 수면 부족이 정서 조절 기능을 저하시키고, 정서적 고통이 다시 수면을 방해하는 양상으로, 수면에 대한 우선적 개입이 전반적인 증상 개선에 기여할 수 있을 것으로 사료됩니다.')

  if (stress && sleep && stress.levelIdx >= 2 && sleep.levelIdx >= 1)
    notes.push('높은 수준의 스트레스와 수면 문제가 동시에 관찰됩니다. 만성 스트레스로 인한 과각성 상태가 수면의 질을 저하시키고, 수면 부족이 스트레스 내성을 약화시키는 악순환이 형성되어 있는 것으로 판단되며, 이 패턴의 지속 시 소진(번아웃)으로 이어질 위험이 있어 조기 개입이 필요한 것으로 보입니다.')

  if (dep && stress && dep.levelIdx >= 2 && stress.levelIdx >= 2)
    notes.push('높은 스트레스가 현재 우울 증상의 주요 촉발 및 유지 요인으로 작용하고 있는 것으로 사료됩니다. 스트레스 원인에 대한 체계적 분석과 함께, 정서 증상에 대한 전문적 치료를 병행하는 것이 효과적일 것으로 판단됩니다.')

  if (anx && sleep && anx.levelIdx >= 2 && sleep.levelIdx >= 2)
    notes.push('불안 증상과 수면 장애가 동반되어 있으며, 과각성 상태가 입면을 방해하고 수면 부족이 다시 불안을 가중시키는 양상이 관찰됩니다. 취침 전 이완 훈련(호흡법, 근육 이완)의 루틴화가 두 증상의 동시 완화에 기여할 수 있을 것으로 보입니다.')

  if (self && stress && self.levelIdx >= 2 && stress.levelIdx >= 2)
    notes.push('자존감 저하와 높은 스트레스가 동반되어 있습니다. 낮은 자기 효능감이 스트레스 상황에서의 대처 시도를 저하시키고, 미해결된 스트레스가 다시 자존감을 훼손하는 악순환 구조가 형성되어 있는 것으로 판단됩니다.')

  return notes
}
