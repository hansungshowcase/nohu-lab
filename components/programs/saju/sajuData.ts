const STEM_N = ['갑','을','병','정','무','기','경','신','임','계'] as const
const BRANCH_N = ['자','축','인','묘','진','사','오','미','신','유','술','해'] as const
const ANIMAL_N = ['쥐','소','호랑이','토끼','용','뱀','말','양','원숭이','닭','개','돼지'] as const
const ELEM_N = ['목','화','토','금','수'] as const
const STEM_EL = [0,0,1,1,2,2,3,3,4,4]
const BRANCH_EL = [4,2,0,0,2,1,1,2,3,3,2,4]

function getYearInfo(year: number) {
  const s = ((year - 4) % 10 + 10) % 10
  const b = ((year - 4) % 12 + 12) % 12
  return {
    s, b, el: STEM_EL[s], bEl: BRANCH_EL[b],
    name: `${STEM_N[s]}${BRANCH_N[b]}`,
    animal: ANIMAL_N[b],
    sameElBoth: STEM_EL[s] === BRANCH_EL[b],
  }
}

// 천간합: 갑기, 을경, 병신, 정임, 무계
function isCombine(a: number, b: number): boolean {
  return (a + 5) % 10 === b || (b + 5) % 10 === a
}
// 천간충: 갑경, 을신, 병임, 정계
function isClash(a: number, b: number): boolean {
  const pairs = [[0,6],[1,7],[2,8],[3,9]]
  return pairs.some(([x,y]) => (a===x && b===y) || (a===y && b===x))
}

